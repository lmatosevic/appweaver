import path from 'node:path';
import fs from 'node:fs';
import { spawn } from 'node:child_process';
import { register } from 'ts-node';
import { TModule, TSchema, Type } from '@sinclair/typebox';
import { ModelToTypeScript } from '@sinclair/typebox-codegen';
import { File } from '@appweaver/core';
import { ResourceModelSchema, capitalize } from '@appweaver/common';

export async function generateTypes(
  outputFile: string = 'generated.d.ts'
): Promise<void> {
  const cwd = process.cwd();
  const resourcesDir = path.join(cwd, 'src', 'resources');
  const typesDir = path.join(cwd, 'src', 'types');

  if (!fs.existsSync(resourcesDir)) {
    console.log('Resources directory not found. Skipping type generation.');
    return;
  }

  if (!fs.existsSync(typesDir)) {
    fs.mkdirSync(typesDir, { recursive: true });
    console.log('Types directory created.');
  }

  // Register ts-node to handle .ts imports
  register({
    transpileOnly: true,
    compilerOptions: {
      module: 'CommonJS'
    }
  });

  const directories = fs
    .readdirSync(resourcesDir, { withFileTypes: true })
    .filter((dirent) => dirent.isDirectory());

  const schemas: Record<string, ResourceModelSchema> = {};

  try {
    for (const dir of directories) {
      const dirName = dir.name;
      const modelPath = path.join(resourcesDir, dirName, 'model.ts');

      if (!fs.existsSync(modelPath)) {
        console.log(
          `Resource ${dirName} does not export model schema. Skipping.`
        );
        continue;
      }

      // Clear cache to ensure fresh model data
      delete require.cache[require.resolve(modelPath)];
      const modelExport = require(modelPath);
      const model: ResourceModelSchema = modelExport.default || modelExport;

      const modelName = model.name || capitalize(dirName);

      schemas[modelName] = model;
    }

    const resourceModels: Record<string, TSchema> = {
      File: File
    };

    for (const [modelName, schema] of Object.entries(schemas)) {
      resourceModels[modelName] = schema.readModel;
      resourceModels[`${modelName}Create`] = schema.createModel;
      resourceModels[`${modelName}Update`] = schema.updateModel;
      resourceModels[`${modelName}Relations`] = schema.relationsModel;
      resourceModels[`${modelName}Files`] = schema.filesModel;
      resourceModels[`${modelName}Virtual`] = schema.virtualModel;
    }

    const module = Type.Module(resourceModels);

    const typesContent: string[] = [
      `// Generated by @appweaver/cli. Please do not edit this file.`,
      ``
    ];

    for (const modelName of Object.keys(resourceModels)) {
      typesContent.push(generateTypeScriptType(module, modelName), ``);
    }

    const outputPath = path.join(typesDir, outputFile);
    fs.writeFileSync(outputPath, typesContent.join('\n'));

    spawn(`prettier --write ${outputPath}`, { stdio: 'inherit', shell: true });

    console.log(`Generated types to ${typesDir}/${outputFile}`);
  } catch (error) {
    console.error(`Failed to generate types:`, error);
  }
}

function generateTypeScriptType(
  module: TModule<{ [p: string]: TSchema }, { [p: string]: TSchema }>,
  name: string
): string {
  const model: TSchema = module.Import(name);
  const types = [model.$defs[name]];
  return ModelToTypeScript.GenerateType({ types }, name);
}
