import fs from 'node:fs';
import fsp from 'node:fs/promises';
import path from 'node:path';
import prettier from 'prettier';
import {
  Command,
  InvalidArgumentError,
  InvalidOptionArgumentError
} from 'commander';
import { generateClient, generateTypes } from '../generators';
import { readSchemaContent, toSchemaObject } from '../utils';
import { FRAMEWORKS } from '../constants';

export function generateCommand(program: Command): void {
  program
    .command('generate')
    .alias('g')
    .description('Generates types and client class.')
    .argument(
      '<schemaPath>',
      'The path to the OpenAPI schema (file or URL)',
      parseSchema
    )
    .option(
      '--outputPath [path]',
      'Output path for both generated types and client class.',
      './generated/client.ts'
    )
    .option(
      '--typesPath [path]',
      'Output path for generated types. (default: output path)'
    )
    .option(
      '--clientPath [path]',
      'Output path for generated client class. (default: output path)'
    )
    .option(
      '--clientName [name]',
      'Name for generated client class. (default: OpenAPI schema title)'
    )
    .option(
      '--framework [name]',
      `The framework for generated client class (${FRAMEWORKS.join(', ')})`,
      parseFramework,
      'fetch'
    )
    .option(
      '--typesOnly',
      'Generate TypeScript types only (without client class).'
    )
    .option(
      '--clientOnly',
      'Generate only client class (without TypeScript types).'
    )
    .option(
      '--noTypes',
      'Generate client class without TypeScript type support.'
    )
    .action(async (schemaPath: string, _, command: Command) => {
      const outputPath = command.getOptionValue('outputPath');
      const typesPath = command.getOptionValue('typesPath') || outputPath;
      const clientPath = command.getOptionValue('clientPath') || outputPath;
      const clientName = command.getOptionValue('clientName');
      const framework = command.getOptionValue('framework');
      const typesOnly = command.getOptionValue('typesOnly');
      const clientOnly = command.getOptionValue('clientOnly');
      const noTypes = command.getOptionValue('noTypes');
      const cwd = process.cwd();

      const schemaContent = await readSchemaContent(schemaPath);
      const schemaObject = await toSchemaObject(schemaContent);

      if (!clientOnly && !noTypes) {
        const typesContent = await generateTypes(schemaObject);
        await formatAndWriteFile(
          typesPath,
          typesContent,
          typesOnly || typesPath !== clientPath
        );

        console.log(`Generated types to ${path.relative(cwd, typesPath)}`);
      }

      if (!typesOnly) {
        const typesImportPath =
          typesPath === clientPath
            ? undefined
            : relativePathFrom(clientPath, typesPath);
        const clientContent = await generateClient(
          schemaObject,
          clientName,
          framework,
          typesImportPath,
          noTypes
        );
        await formatAndWriteFile(
          clientPath,
          clientContent,
          true,
          typesPath === clientPath
        );

        console.log(`Generated client to ${path.relative(cwd, clientPath)}`);
      }
    });
}

async function formatAndWriteFile(
  filePath: string,
  content: string,
  addGeneratedComment: boolean = true,
  appendIfExists: boolean = false
): Promise<void> {
  const dirPath = path.resolve(path.dirname(filePath));

  const dirExists = await pathExists(dirPath);
  if (!dirExists) {
    await fsp.mkdir(dirPath, { recursive: true });
  }

  const prettierConfig = await prettier.resolveConfig(filePath);
  let formattedContent = await prettier.format(content, {
    ...(prettierConfig ?? { singleQuote: true, trailingComma: 'none' }),
    filepath: filePath
  });

  const fileExists = await pathExists(filePath);
  if (appendIfExists && fileExists) {
    const existingContent = await fsp.readFile(filePath, 'utf8');
    formattedContent = formattedContent + '\n' + existingContent;
  }

  if (addGeneratedComment) {
    formattedContent =
      '// Generated by Appweaver. Please do not edit this file manually.\n\n' +
      formattedContent;
  }

  await fsp.writeFile(filePath, formattedContent, 'utf8');
}

async function pathExists(filePath: string): Promise<boolean> {
  try {
    await fsp.access(filePath, fsp.constants.F_OK);
    return true;
  } catch (e) {
    return false;
  }
}

export function relativePathFrom(firstPath: string, otherPath: string): string {
  const firstDir = path.dirname(path.resolve(firstPath));
  const otherAbs = path.resolve(otherPath);

  let rel = path.relative(firstDir, otherAbs);

  if (!rel.startsWith('.') && rel !== '') {
    rel = `.${path.sep}${rel}`;
  } else if (rel === '') {
    rel = '.';
  }

  if (rel.endsWith('.ts')) {
    rel = rel.replace('.d.ts', '.ts').replace('.ts', '');
  }

  return rel.replace(/\\/g, '/');
}

function parseSchema(value: string): string {
  let schemaUrl: URL | undefined;

  try {
    schemaUrl = new URL(value);
  } catch {
    // Schema path is not in URL format
  }

  if (schemaUrl) {
    if (!['http:', 'https:', 'file:'].includes(schemaUrl.protocol)) {
      throw new InvalidArgumentError(
        `Unsupported schema URL protocol: ${schemaUrl.protocol}`
      );
    }
    return value;
  }

  if (!fs.existsSync(value)) {
    throw new InvalidArgumentError(
      `Schema path "${value}" does not exist or is not a valid URL.`
    );
  }

  return value;
}

function parseFramework(value: string): string {
  const lowerFramework = value.toLowerCase();
  if (!FRAMEWORKS.includes(lowerFramework as any)) {
    throw new InvalidOptionArgumentError(
      `Must be one of following: ${FRAMEWORKS.join(', ')}.`
    );
  }

  return lowerFramework;
}
